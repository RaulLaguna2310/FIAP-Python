# Operadores Relacionais:
# - "==" = Igualdade
# - ">" = Maior que...
# - "<" = Menor que...
# - "!=" = Diferente
# - ">=" = Maior ou igual que...
# - "<=" = Menor ou igual que...

# Expressões Relacionais:
# - "expressão" == "expressão": retorna True quando os valores das expressões forem iguais
# - "expressão" != "expressão": retorna True quando os valores das expressões forem diferentes
# - "expressão" > "expressão": retorna True quando a espressão da esquerda for maior que a outra
# - "expressão" < "expressão": retorna True quando a espressão da esquerda for menor que a outra
# - "expressão" >= "expressão": retorna True quando a espressão da esquerda for maior ou igual que a outra
# - "expressão" <= "expressão": retorna True quando a espressão da esquerda for menor ou igual que a outra
# - Expressões relacionais produzem um resultado booleano (True ou False)

