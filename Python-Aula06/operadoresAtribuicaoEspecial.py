# Operadores de Atribuição Especial
# São usados para realizar operações de atribuição de forma mais concisa.
 
# Operador | Exemplo | Esquivalência
# +=       | x += 1  | x = x + 1
# -=       | x -= 1  | x = x - 1
# *=       | x *= 2  | x = x * 2
# /=       | x /= 2  | x = x / 2
# **=      | x **= 2 | x = x ** 2
# //=      | x //= 2 | x = x // 2